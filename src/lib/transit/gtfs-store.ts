export async function getAllTransfers(): Promise<Footpath[]> {
    const db = await openTransitDB();
    const [gtfs, auto] = await Promise.all([
        db.getAll("transfersGtfs"),
        db.getAll("transfersAuto"),
    ]);
    return [...gtfs, ...auto];
}

// ---------------------------------------------------------------------------
// Auto-transfer management
// ---------------------------------------------------------------------------

export async function replaceAutoTransfers(
    transfers: Footpath[],
): Promise<void> {
    const db = await openTransitDB();
    const tx = db.transaction("transfersAuto", "readwrite");
    await tx.objectStore("transfersAuto").clear();
    await Promise.all(
        transfers.map((t) => tx.objectStore("transfersAuto").put(t)),
    );
    await tx.done;
}

// ---------------------------------------------------------------------------
// Diagnostics — used by the Transit Systems dialog to show space usage
// ---------------------------------------------------------------------------

export async function estimateUsage(): Promise<{
    usage?: number;
    quota?: number;
}> {
    if (
        typeof navigator === "undefined" ||
        !navigator.storage?.estimate
    ) {
        return {};
    }
    const { usage, quota } = await navigator.storage.estimate();
    return { usage, quota };
}
